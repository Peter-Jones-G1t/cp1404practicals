try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Denominator cannot be zero.")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
print("Finished.")

#1. ValueError will occur when anything other than an integer is entered e.g letters or special characters
#2. ZeroDivisionError will occur when a 0 is entered into the denominator input (trying to divide by 0)
